#!/bin/sh
# dwm-titus autostart — single unified autostart script
# Phase 1: Blocking setup (must complete before windows appear)
# Phase 2: Background services (compositor, notifications, polybar, tray apps)

# ── Phase 1: Blocking ──────────────────────────────────────────────────────────
# Disable DPMS and screen blanking (prevents GPU/display wake issues)
xset s off
xset s noblank
xset -dpms

# Export display env to systemd/dbus in parallel (both are IPC round-trips)
if command -v systemctl >/dev/null 2>&1; then
    systemctl --user import-environment DISPLAY XAUTHORITY &
fi
dbus-update-activation-environment --systemd DISPLAY XAUTHORITY 2>/dev/null &
wait

# ── Phase 2: Background services ───────────────────────────────────────────────
{

# Source theme environment so tray apps (e.g. flameshot) inherit QT_QPA_PLATFORMTHEME
# This file is generated by theme-apply.sh and updated on every theme change.
THEME_ENV="${XDG_CONFIG_HOME:-$HOME/.config}/dwm-titus/theme-env.sh"
[ -f "$THEME_ENV" ] && . "$THEME_ENV"

# Wallpaper (moved here from Phase 1 — does not need to block dwm startup)
feh --randomize --bg-fill ~/Pictures/backgrounds/* 2>/dev/null &

# Compositor
picom -b 2>/dev/null &

# Notification daemon
dunst 2>/dev/null &

# Polkit authentication agent (try common agents)
for agent in \
    /usr/lib/mate-polkit/polkit-mate-authentication-agent-1 \
    /usr/lib/polkit-gnome/polkit-gnome-authentication-agent-1 \
    /usr/libexec/polkit-gnome-authentication-agent-1 \
    /usr/lib/polkit-kde-authentication-agent-1 \
    /usr/libexec/polkit-kde-authentication-agent-1 \
    /usr/bin/lxpolkit \
    /usr/lib/lxpolkit/lxpolkit; do
    if [ -x "$agent" ]; then
        "$agent" &
        break
    fi
done

# Launch Polybar (skip if already running, e.g. started by .xinitrc)
if ! pgrep -x polybar >/dev/null 2>&1; then
    for pb_launch in \
        "$HOME/.config/polybar/launch.sh" \
        "$HOME/.local/share/dwm-titus/polybar/launch.sh"; do
        if [ -x "$pb_launch" ]; then
            "$pb_launch" &
            break
        fi
    done
fi

# Verify polybar systray module is loaded before launching tray apps
# Polls _NET_SYSTEM_TRAY_S0 X selection to confirm the tray manager is ready
polybar_tray_ready() {
    python3 -c "
import ctypes, ctypes.util, sys
lib = ctypes.util.find_library('X11')
if not lib: sys.exit(1)
x11 = ctypes.cdll.LoadLibrary(lib)
x11.XOpenDisplay.restype = ctypes.c_void_p
d = x11.XOpenDisplay(None)
if not d: sys.exit(1)
atom = x11.XInternAtom(d, b'_NET_SYSTEM_TRAY_S0', True)
if not atom:
    x11.XCloseDisplay(d); sys.exit(1)
owner = x11.XGetSelectionOwner(d, atom)
x11.XCloseDisplay(d)
sys.exit(0 if owner else 1)
" 2>/dev/null
}

tray_ok=0
i=0
while [ $i -lt 50 ]; do
    if polybar_tray_ready; then
        tray_ok=1
        break
    fi
    sleep 0.2
    i=$((i + 1))
done

if [ $tray_ok -eq 1 ]; then
    dex -a 2>/dev/null
else
    echo "dwm-titus: polybar systray not detected, launching dex anyway" >&2
    dex -a 2>/dev/null
fi

} &
